#include "fillit.h"

void		ft_del(char **min)
{
	int		i;

	i = 0;
	while (min[i])
	{
		free(min[i]);
		min[i] = 0;
		i++;
	}
	free(min[i]);
	min[i] = 0;
	free(min);
	min = 0;
}
